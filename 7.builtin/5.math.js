// Math
// static property
console.log(Math.E); // 오일러의 상수, 자연로그의 밑
console.log(Math.PI); // 원주율 PI값

// static method
console.log('-----------------method');
console.log(Math.abs(-10)); // 절대값
console.log(Math.ceil(1.4)); // 소수점 이하를 올림
console.log(Math.floor(1.4)); // 소수점 이하를 내림
console.log(Math.round(1.4)); // 소수점 이하를 반올림
console.log(Math.round(1.7)); // 소수점 이하를 반올림
console.log(Math.trunc(1.75234)); // 소수점 이하를 버림
console.log(Math.max(1, 2, 3, 4)); // 최댓값 찾기
console.log(Math.min(1, 2, 3, 4)); // 최솟값 찾기
console.log(Math.pow(3, 2)); // 거듭제곱(3 ** 2)
console.log(Math.sqrt(9)); // 제곱근
console.log('-----------------');
console.log(Math.random()); // 랜덤값 반환(0-1사이)
console.log(Math.floor(Math.random() * 10 + 1)); // 1-10사이
console.log(Math.floor(Math.random() * 100 + 1)); // 1-100사이
